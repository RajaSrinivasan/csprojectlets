                return false;
            }

            DateTime today = DateTime.Now;
            string modificationTime = today.ToString();

            FileContents = Encoding.UTF8.GetString(PlainText);
            string memberpwd = cli.GetPassword("Password for the context " + cli.Context + ":" + cli.Username + " : ");

            memberships = Membership.ParseDb(FileContents);
            Membership m = Find(memberships, cli.Context, cli.Username);
            if (m != null)
            {
                m.SetPassword(memberpwd);
            }
            else
            {
                Console.WriteLine($"Unknown context {cli.Context} username {cli.Username}");
                return false;
            }
            FileContents = "# Modified user on " + modificationTime + "\n" +
                Membership.All(memberships);
            PlainText = Encoding.UTF8.GetBytes(FileContents);
            return Save();
        }

