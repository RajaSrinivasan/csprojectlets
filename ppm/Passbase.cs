            DateTime today = DateTime.Today;
            CreationTime = today.ToString();
            FileContents = "# Creation Date " + CreationTime + " by Srini -------------------------------\n";
            PlainText = Encoding.UTF8.GetBytes(FileContents);
            return Save();
        }

